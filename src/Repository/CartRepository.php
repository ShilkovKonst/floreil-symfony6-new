<?php

namespace App\Repository;

use App\Entity\Cart;
use Doctrine\Bundle\DoctrineBundle\Repository\ServiceEntityRepository;
use Doctrine\ORM\QueryBuilder;
use Doctrine\Persistence\ManagerRegistry;

/**
 * @extends ServiceEntityRepository<Cart>
 *
 * @method Cart|null find($id, $lockMode = null, $lockVersion = null)
 * @method Cart|null findOneBy(array $criteria, array $orderBy = null)
 * @method Cart[]    findAll()
 * @method Cart[]    findBy(array $criteria, array $orderBy = null, $limit = null, $offset = null)
 */
class CartRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Cart::class);
    }

    public function save(Cart $entity, bool $flush = false): void
    {
        $this->getEntityManager()->persist($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function remove(Cart $entity, bool $flush = false): void
    {
        $this->getEntityManager()->remove($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function findOneByUserAndProduct($user, $product): ?Cart
    {
        $query = $this->createQueryBuilder('cart')
        ->andWhere('cart.users = :users')
        ->setParameter('users', $user)
        ->andWhere('cart.products = :products')
        ->setParameter('products', $product)
        ->getQuery()
        ->getOneOrNullResult();

        return $query;
    }
    public function findByUser($user): array
    {
        $query = $this->createQueryBuilder('cart')
        ->andWhere('cart.users = :users')
        ->setParameter('users', $user)
        ->orderBy('cart.created_at', 'DESC')
        ->getQuery()
        ->getResult();

        return $query;
    }

